CREATE TABLE IF NOT EXISTS user_like (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    ip TEXT NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    page_id INTEGER NOT NULL,
    FOREIGN KEY(page_id) REFERENCES page(id)
    UNIQUE(ip, page_id)
);